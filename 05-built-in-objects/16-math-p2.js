/*
 * Math — Parte 2
 *  - também tem métodos de arredondamento como: floor(), ceil() e round()
 *  - métodos para calcular potência e raiz quadrada: pow() e sqrt()
*/

console.log(Math.floor(12.384)) // 12
console.log(Math.ceil(12.384)) // 13
console.log(Math.round(12.384)) // 12

console.log(Math.pow(2, 10)) // 1024

console.log(Math.sqrt(81)) // 9
