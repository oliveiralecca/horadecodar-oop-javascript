/*
 * Math — Parte 1
 *  - um objeto com propriedades e métodos matemáticos
 *  - podemos saber o valor de PI e até gerar números aleatórios
*/

console.log(Math.PI) // 3.141592653589793
console.log(Math.LN2) // 0.6931471805599453 -> logaritmo na base 2
console.log(Math.random()) // 0.123456789 -> gera um número aleatório entre 0 e 1
console.log(Math.floor(Math.random() * 101)) // 12 -> gera um número aleatório entre 0 e 100
