/*
 * String — Parte 1
 *  - podemos criar uma string com o 'new'
 *  - temos acesso à propriedade length, que retorna o número de caracteres
 *  - podemos acessar um caractere pelo seu índice
*/

const s1 = 'JavaScript'
const s2 = new String('Python')

console.log(s1.length) // 10
console.log(s2.length) // 6

console.log(s1[0]) // J
console.log(s2[0]) // P
